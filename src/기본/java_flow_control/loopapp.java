package 기본.java_flow_control;

public class loopapp {

    public static void main(String[] args) {

        int i =0;
        while(i < 3){
            System.out.println(2);
            System.out.println(3);
            i++;
        }

        for(int j = 0; j < 3; j++){
            System.out.println(2);
            System.out.println(3);
        }

        System.out.println(4);
    }
}
